function add(a, b) {
    return a + b;
};

function subtract(a, b) {
    return a - b;
};

function multiply(a, b) {
    return a * b;
};

function divide(a, b) {
    return a / b;
};

function increment(n = 5) {
    n++;
    return n;
};

function decrement(n = 10) {
    n--;
    return n;
};

function makeInt(n) {
    parseInt('n');
    return parseInt(n, 10);
};

function preserveDecimal(n) {
     parseFloat('n');
     return parseFloat(n);
};